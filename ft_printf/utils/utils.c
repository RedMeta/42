#include "../ft_printf.h"

void	ft_putstr(char *s)
{
	int i;

	if (!s)
		return ;
	i = -1;
	while (s[++i])
		ft_putchar(s[i]);
}

void	ft_putchar(char c)
{
		write(1, &c, 1);
}