int	ft_numlen_base(int i, int base)
{
	int	n;

	if (i == -2147483648)
		return (11);
	n = ((i > 0) * 0) + (!(i > 0) * 1);
	while (i)
	{
		i /= base;
		n++;
	}
	return (n);
}
